# ╔═╡ b7f8d612-1d3d-414f-8f94-27fb9154daa5
data_directory = joinpath(".","data","nyc")

# ╔═╡ 0c3d8b32-d16b-40a0-a67b-7d2df89371d7
begin
function reproject_points!(
	geom_obj::ArchGDAL.IGeometry,
	source::ArchGDAL.ISpatialRef,
	target::ArchGDAL.ISpatialRef)

	ArchGDAL.createcoordtrans(source, target) do transform
		ArchGDAL.transform!(geom_obj, transform)
	end
    return geom_obj
end

function reproject_points!(
	geom_obj::Union{
		Vector{ArchGDAL.IGeometry},
		Vector{ArchGDAL.IGeometry{ArchGDAL.wkbPoint}},
		Vector{ArchGDAL.IGeometry{ArchGDAL.wkbMultiPolygon}},
		Vector{ArchGDAL.IGeometry{ArchGDAL.wkbPolygon}}
	},
	source::ArchGDAL.ISpatialRef,
	target::ArchGDAL.ISpatialRef)

	ArchGDAL.createcoordtrans(source, target) do transform
		ArchGDAL.transform!.(geom_obj, Ref(transform))
	end
    return geom_obj
end
end

# ╔═╡ a5dc4d36-c730-48ba-b786-9ce88eb906b7
source = ArchGDAL.importEPSG(4326, order=:trad)

# ╔═╡ 10a605d8-8e01-4540-8cab-5eaff4c81ec0
target = ArchGDAL.importEPSG(3857)

# ╔═╡ cf89691e-4b72-4e58-bcc7-1cc36e998743
md"""
First going to load the nyc footprint data
"""

# ╔═╡ 487a4b22-d051-4dfa-8c6b-5b9fa28de865
begin
	footprints_directory = joinpath(data_directory, "footprints")
	building_footprints_path = joinpath(
		footprints_directory, 
		"building_footprints.geojson"
	)
	footprints = GeoDataFrames.read(building_footprints_path)
	rename!(footprints, "base_bbl" => "bbl")
	filter!(x -> tryparse(Float64, x.heightroof) !== nothing, footprints)
	filter!(x -> tryparse(Float64, x.groundelev) !== nothing, footprints)
	footprints[!,:heightroof] = Base.parse.(Float64, footprints.heightroof)
	footprints[!,:groundelev] = Base.parse.(Float64, footprints.groundelev)
	select!(footprints,
	[
		:geom,
		:bbl,
		:heightroof,
		:cnstrct_yr,
		:groundelev,
		:bin
	])
	dropmissing!(footprints, [:bbl,:bin])
	reproject_points!(footprints.geom, source, target)
	footprints[!,:geom_point] = [ArchGDAL.centroid(x) for x in footprints.geom]
	footprints
end

# ╔═╡ 0a17908b-d0cb-452a-9df0-a05c451b6681
begin
	# NOTE: the index terms generated are unique to this file
	microsoft_building_footprints_path = joinpath(
		footprints_directory,
		"NewYorkCity.geojson"
	)
	microsoft_footprints = GeoDataFrames.read(microsoft_building_footprints_path)

	# This puts the most recent footprints identities first
	microsoft_footprints = @chain microsoft_footprints begin
		sort(:release, rev=true)
	end
	reproject_points!(microsoft_footprints.geom, source, target)
	unique(microsoft_footprints, :capture_dates_range)
end

# ╔═╡ ad1436b4-de3f-49fe-8564-708f11c69104
md"""
Now the matching algorithm
"""

# ╔═╡ 81c1d5cf-e0ae-4cfd-840b-592715819b3a
unique_bbl_footprints = unique(footprints, :bbl)[1:10,:]

# ╔═╡ f5268e67-46bd-44b5-813d-66c43617543f
begin
	building_footprint_match = 
		Vector{Union{Missing, Int64}}(undef, nrow(unique_bbl_footprints))
	
	Threads.@threads for (building_index, building_point) = collect(enumerate(unique_bbl_footprints.geom_point))
		building_footprint_match[building_index] = missing
		for (footprint_index,footprint) in collect(enumerate(microsoft_footprints.geom))
			if GeoDataFrames.contains(footprint, building_point)
				building_footprint_match[building_index] = footprint_index
				break
			end
		end
	end
end

# ╔═╡ 14dfb674-2794-4944-9117-1182ece26ffc
begin
	co = select(unique_bbl_footprints, [:bbl,:bin])
	co.microsoft = building_footprint_match
	dropmissing!(co, :microsoft)
	co.microsoft_geom = microsoft_footprints[co.microsoft,:].geom
	select!(co, Not("microsoft"))
end

# ╔═╡ a65f13fe-66c5-4ed8-8740-2c1d8dd6a2f6
GeoDataFrames.write("microsoft_footprint_match_nyc.shp", co; layer_name="data", geom_column=:microsoft_geom, crs=GFT.EPSG(3857))

